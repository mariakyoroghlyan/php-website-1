                const expandedHeight = $cardText[0].scrollHeight;
                const fullCardHeight = $timelineCard[0].scrollHeight;

                // Add the height directly with CSS
                $timelineCard.css('height', fullCardHeight + expandedHeight + 'px');
                $timelineCardBox.css('height', expandedHeight + 'px');
                $cardText.removeClass('concat_text');
                $('.see_more_svg2').css('transform', 'rotate(45deg)');

                if (screenWidth <= 500) {
                    $timelineCard.find('.one').animate({ left: '0' }, 300);
                    $timelineCard.find('.two').animate({ left: '6vw' }, 300);
                } else {
                    $timelineCard.find('.one').animate({ left: '0' }, 300);
                    $timelineCard.find('.two').animate({ left: '3vw' }, 300);
                }
            } else {
                const collapsedHeight = screenWidth <= 500
                    ? '67.834vw'
                    : screenWidth <= 1024
                        ? '32.073vw'
                        : '14.843vw';

                // Use the CSS transition for smooth collapsing
                $timelineCard.css('height', collapsedHeight);
                $timelineCardBox.css('height', screenWidth <= 1024 && screenWidth >= 500
                    ? '4.634vw'
                    : screenWidth <= 500
                        ? '11.233vw'
                        : '2.291vw');

                setTimeout(function () {
                    $cardText.addClass('concat_text');
                }, 300);

                $('.see_more_svg2').css('transform', 'rotate(0deg)');

                if (screenWidth <= 500) {
                    $timelineCard.find('.one').animate({ left: '-6vw' }, 300);
                    $timelineCard.find('.two').animate({ left: '0' }, 300);
                } else {
                    $timelineCard.find('.one').animate({ left: '-3vw' }, 300);
                    $timelineCard.find('.two').animate({ left: '0' }, 300);
                }
            }
        });
    });



